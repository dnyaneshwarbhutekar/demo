package controlstatement;

public class switch5 {
	public static void main(String[] args) {
		int i=1;
	switch(i) {
		case 1: 
		System.out.println("---mon---");
		break;
		case 2: 
		System.out.println("---tue---");
		break;
		case 3:
		System.out.println("---wed---");
		break;
		case 4:
		System.out.println("---thus---");
		break;
		
		}
		
	}

}
