package controlstatment;

public class elseif {
	public static void main(String[] args) {
		int a=20;
		if(a>=50) {
			System.out.println("very good");
			
		}else if (a>=40) {
			System.out.println("good");
			
		}else if (a>=30) {
			System.out.println("bad");
		}else{
			System.out.println("invalied");
		}
		
	}

}
