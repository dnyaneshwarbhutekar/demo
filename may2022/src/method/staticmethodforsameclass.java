package method;

public class staticmethodforsameclass {
	public static void main(String[] args) {
		add();
		mul();
	}
	public static void add() {
		int a=50;
		int b= 50;
		System.out.println(a+b);
		
	}
	public static void mul() {
		int c=10;
		int d=20;
		System.out.println(c*d);
		
		
	}

}
